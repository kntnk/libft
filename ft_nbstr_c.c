#include "libft.h"

int		ft_nbstr_c(char const *s, char c)
{
	int		i;

	i = 0;
	while (s[i] != '\0')
	{
		while ((s[i] != '\0' && s[i] == c) || (s[i] != '\0' && s[i] != c))
			i++;
		if (s[i] != '\0')
			i++;
	}
	return (i);
}
