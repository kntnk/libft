#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	if (ft_strchr(str, c) == NULL)
		return (NULL);
	while (*str != '\0')
		str++;
	while (*str != c)
		str--;
	return ((char*)str);
}
