#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	size_t	len;
	int		i;

	if (s != NULL && f != NULL)
	{
		len = ft_strlen(s);
		str = (char *)malloc(sizeof(*str) * len);
		i = 0;
		while (s[i] != '\0')
		{
			str[i] = f(s[i]);
			i++;
		}
		return (str);
	}
	return (NULL);
}
